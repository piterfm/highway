const getHighway = (number) => {
    let prefix = null;
    let name = null;
    
    number = parseInt(number, 10);
    
    if (SR.has(number)) {
        prefix = 'sr';
        name = 'STATE ROUTE';
    }
    else if (I.has(number)) {
        prefix = 'i';
        name = 'INTERSTATE';
    }
    else if (US.has(number)) {
        prefix = 'us';
        name = 'US HIGHWAY';
    }
    
    return prefix === null ? null :
        {
            code: `${prefix}${number}`,
            name: `${name} ${number}`,
        };
};

module.exports = getHighway;
